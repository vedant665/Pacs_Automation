def pytest_sessionfinish(session, exitstatus):
    """Generate validation Excel report after all tests complete."""
    from pages.company_onboarding.test.validation_results_store import validation_results
    from pages.company_onboarding.test.validation_report_generator import generate_validation_report

    if not validation_results:
        return

    try:
        filepath = generate_validation_report(validation_results, REPORT_DIR)
        total = len(validation_results)
        passed = sum(1 for r in validation_results if r["status"] == "PASSED")
        failed = sum(1 for r in validation_results if r["status"] == "FAILED")
        bugs = sum(1 for r in validation_results if r.get("is_bug"))

        log.separator()
        log.info(" COMPANY ONBOARDING VALIDATION REPORT")
        log.info(f" Total: {total} | Passed: {passed} | Failed: {failed} | Bugs Found: {bugs}")
        log.info(f" Report: {filepath}")
        log.separator()

        validation_results.clear()
    except Exception as e:
        log.error(f"Failed to generate validation report: {e}")