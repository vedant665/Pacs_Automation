    def test_01_header_untouched_bug(self, logged_in_driver):
        """
        BUG: Required header fields (Company Name, Entity Group, etc.)
        don't show validation errors when Next is clicked without
        touching them first.

        Angular marks them ng-invalid but NOT ng-touched,
        so mat-error elements remain hidden.
        """
        driver = logged_in_driver
        page = self._page(driver)

        self._open_fresh_form(page)

        # Click Next WITHOUT touching any header field
        try:
            page._click_next()
        except Exception:
            pass
        page.wait_seconds(1)

        shot = self._take_screenshot(driver, "BUG_01_header_untouched")

        header_fields = ["Company Name", "Company Short Name"]
        for field_name in header_fields:
            state = self._check_field_state(driver, field_name)
            is_bug = (
                state.get("is_invalid", False)
                and not state.get("is_touched", False)
                and not state.get("is_showing_error", False)
            )

            self._record(
                test_name=f"BUG: Untouched '{field_name}' should show error",
                category="Bug Discovery",
                field=field_name,
                bad_value="(empty, untouched)",
                expected="mat-error visible on Next click",
                actual=(
                    f"ng-invalid={state.get('is_invalid')}, "
                    f"touched={state.get('is_touched')}, "
                    f"showing_error={state.get('is_showing_error')}"
                ),
                status="FAILED" if is_bug else "PASSED",
                is_bug=is_bug,
                impact="User gets no feedback that required field is empty until clicking into it.",
                screenshot=shot,
            )

        self._close_form(page)

