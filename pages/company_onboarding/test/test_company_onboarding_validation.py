    def _check_field_state(self, driver, field_name):
        """
        Check Angular validation state of an input/textarea field.
        Returns: {is_invalid, is_touched, is_showing_error, error_text}
        """
        try:
            input_el = driver.find_element(
                By.CSS_SELECTOR,
                f"input[name='{field_name}'], textarea[name='{field_name}']",
            )
            classes = input_el.get_attribute("class") or ""

            error_text = ""
            try:
                form_field = input_el.find_element(
                    By.XPATH, "./ancestor::mat-form-field"
                )
                mat_errors = form_field.find_elements(By.CSS_SELECTOR, "mat-error")
                visible = [e for e in mat_errors if e.is_displayed()]
                if visible:
                    error_text = " | ".join(e.text for e in visible if e.text.strip())
            except Exception:
                pass

            return {
                "is_invalid": "ng-invalid" in classes,
                "is_touched": "ng-touched" in classes,
                "is_dirty": "ng-dirty" in classes,
                "is_showing_error": bool(error_text),
                "error_text": error_text,
            }
        except Exception as e:
            return {"error": str(e)}

    def _check_sweetalert(self, driver, timeout=5):
        """Check if SweetAlert popup is visible."""
        try:
            title_el = WebDriverWait(driver, timeout).until(
                EC.visibility_of_element_located((By.CSS_SELECTOR, "#swal2-title"))
            )
            msg = driver.find_element(By.CSS_SELECTOR, "#swal2-html-container").text
            has_btn = driver.find_elements(By.CSS_SELECTOR, ".swal2-confirm")
            return {
                "appeared": True,
                "title": title_el.text,
                "message": msg,
                "has_download_button": len(has_btn) > 0,
            }
        except Exception:
            return {"appeared": False}

    def _dismiss_sweetalert(self, driver):
        try:
            cancel = driver.find_element(By.CSS_SELECTOR, ".swal2-cancel")
            cancel.click()
            time.sleep(1)
        except Exception:
            pass

    def _download_validation_errors(self, driver):
        """Click Download Errors on SweetAlert, return saved file path."""
        try:
            before = glob.glob(os.path.join(DOWNLOAD_DIR, "*.xlsx"))
            btn = driver.find_element(By.CSS_SELECTOR, ".swal2-confirm")
            btn.click()
            time.sleep(3)
            after = glob.glob(os.path.join(DOWNLOAD_DIR, "*.xlsx"))
            new_files = [f for f in after if f not in before]
            if new_files:
                newest = max(new_files, key=os.path.getmtime)
                log.info(f"Downloaded errors Excel: {newest}")
                return newest
        except Exception as e:
            log.warning(f"Could not download errors: {e}")
        return None

    def _close_form(self, page):
        try:
            page.click_cancel_or_dismiss_dialog()
            page.wait_seconds(1)
        except Exception:
            pass

    def _run_field_test(self, driver, page, locator, field_name, bad_value, desc, category="Step 1"):
        """
        Test one field: type bad value, click Next, check for mat-error.
        Returns True if validation error was shown.
        """
        # Type bad value
        page.type_text(locator, bad_value, clear_first=True)
        page.wait_seconds(0.3)

        # Click Next (should be blocked by validation)
        try:
            page._click_next()
        except Exception:
            pass
        page.wait_seconds(1)

        # Check field state
        state = self._check_field_state(driver, field_name)
        showing = state.get("is_showing_error", False)
        err_text = state.get("error_text", "")

        shot = self._take_screenshot(driver, f"val_{field_name.replace(' ', '_').lower()}")

        self._record(
            test_name=f"{category}: {desc}",
            category=category,
            field=field_name,
            bad_value=repr(bad_value),
            expected="mat-error visible on Next click",
            actual=f"showing={showing}, error='{err_text}'",
            status="PASSED" if showing else "FAILED",
            screenshot=shot,
        )

        return showing

